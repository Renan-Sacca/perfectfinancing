              Row(
                mainAxisSize: MainAxisSize.max,
                children: [
                  Expanded(
                    child: TextFormField(
                      controller: textController2,
                      obscureText: false,
                      decoration: InputDecoration(
                        hintText: 'Digite o valor:',
                        enabledBorder: UnderlineInputBorder(
                          borderSide: BorderSide(
